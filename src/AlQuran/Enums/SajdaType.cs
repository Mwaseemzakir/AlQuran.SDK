namespace AlQuran.Enums;

/// <summary>
/// Specifies the type of Sajda (prostration) in the Quran.
/// </summary>
public enum SajdaType
{
    /// <summary>
    /// Obligatory prostration (Wajib). Scholars agree this sajda is mandatory when recited.
    /// </summary>
    Obligatory = 0,

    /// <summary>
    /// Recommended prostration (Mustahabb/Sunnah). Performing this sajda is recommended but not mandatory.
    /// </summary>
    Recommended = 1
}
