namespace AlQuran.SDK.Enums;

/// <summary>
/// Specifies the place of revelation for a Surah.
/// </summary>
public enum RevelationType
{
    /// <summary>
    /// Revealed in Mecca before the Hijrah (migration to Medina).
    /// </summary>
    Meccan = 0,

    /// <summary>
    /// Revealed in Medina after the Hijrah (migration from Mecca).
    /// </summary>
    Medinan = 1
}
